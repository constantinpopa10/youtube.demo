package dessignPatterns.decorator.headfirst;

public abstract class Beverage {

	public enum Size {
		SMALL, MEDIUM, LARGE
	}
	
	protected Size size;
	
	

	public Size getSize() {
		return size;
	}

	public void setSize(Size size) {
		this.size = size;
	}

	String description = "Unknown beverage";

	public String getDescription() {
		return description;
	}

	public abstract double cost();
}
